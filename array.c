static inline VALUE
ary_alloc(VALUE klass)
{
    CFMutableArrayRef ary;

    ary = CFArrayCreateMutable(NULL, 0, &kCFTypeArrayCallBacks);
    if (klass != 0 && klass != rb_cNSArray && klass != rb_cNSMutableArray)
	*(Class *)ary = (Class)klass;

    CFMakeCollectable(ary);

    return (VALUE)ary;
}

VALUE
rb_ary_new_fast(int argc, ...) 
{
    va_list ar;
    VALUE ary;
    int i;

    ary = ary_alloc(0);

    va_start(ar, argc);
    for (i = 0; i < argc; i++) {
	VALUE item = va_arg(ar, VALUE);
	CFArrayAppendValue((CFMutableArrayRef)ary, (const void *)RB2OC(item));
    }
    va_end(ar);

    return ary;
}

static VALUE
ary_new(VALUE klass, long len)
{
    VALUE ary;

    if (len < 0) {
	rb_raise(rb_eArgError, "negative array size (or size too big)");
    }
    if (len > LONG_MAX / sizeof(VALUE)) {
	rb_raise(rb_eArgError, "array size too big");
    }
    ary = ary_alloc(klass);

    return ary;
}

VALUE
rb_ary_new2(long len)
{
    return ary_new(0, len);
}

VALUE
rb_ary_new(void)
{
    return rb_ary_new2(ARY_DEFAULT_SIZE);
}

#include <stdarg.h>

VALUE
rb_ary_new3(long n, ...)
{
    va_list ar;
    VALUE ary;
    long i;

    ary = rb_ary_new2(n);

    va_start(ar, n);
    for (i=0; i<n; i++) {
	rb_ary_insert(ary, i, va_arg(ar, VALUE));
    }
    va_end(ar);

    return ary;
}

VALUE
rb_ary_new4(long n, const VALUE *elts)
{
    VALUE ary;

    ary = rb_ary_new2(n);
    if (n > 0 && elts != NULL) {
	long i;
	void **vals = (void **)alloca(n * sizeof(void *));
	for (i = 0; i < n; i++) {
	    vals[i] = RB2OC(elts[i]);
	}
	CFArrayReplaceValues((CFMutableArrayRef)ary, CFRangeMake(0, 0), (const void **)vals, n);
    }

    return ary;
}

